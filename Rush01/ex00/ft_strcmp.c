int			ft_strcmp(char *s1, char *s2)
{
	int		i;
	int		score1;
	int		score2;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != '.')
			score1 = score1 + 1;
		if (s2[i] != '.')
			score2 = score2 + 1;
		i = i + 1;
	}
	return (score1 - score2);
}
