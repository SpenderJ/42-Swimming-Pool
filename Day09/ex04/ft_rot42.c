char	*ft_rot42(char *str)
{
	int		i;
	int		c;
	int		n;

	i = -1;
	n = 0;
	while (str[++i] != '\0')
	{
		c = -1;
		if (str[i] >= 33 && str[i] <= 126)
		{
			while (++c < 42 && str[i] < 126)
				str[i] = str[i] + 1;
			if (c != 42)
				str[i] = 33;
			while (++c < 42)
				str[i] = str[i] + 1;
		}
	}
	return (str);
}
