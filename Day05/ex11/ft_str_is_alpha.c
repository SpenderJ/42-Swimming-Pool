int		ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	if (!(str) || str[0] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] < 'A' || str[i] > 'z' || (str[i] > 'Z' && str[i] < 'a'))
			return (0);
		i = i + 1;
	}
	return (1);
}
