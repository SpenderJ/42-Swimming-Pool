char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	a;

	a = 0;
	while (src[a] != '\0' && a < n)
	{
		dest[a] = src[a];
		a = a + 1;
	}
	while (a < n)
	{
		dest[a] = '\0';
		++a;
	}
	return (dest);
}
