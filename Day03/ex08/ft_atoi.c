int		ft_atoi(char *str)
{
	int	i;
	int	a;
	int	c;
	int	d;

	a = 0;
	i = -1;
	c = 1;
	d = 0;
	while (str[++i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			++a;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		d = (d * c) + (str[i] - 48);
		c = 10;
		i = i + 1;
	}
	if (a == 0)
		return (d);
	else if (a == 1)
		return (-d);
	return (0);
}
